function leader(data, metric) {
  return [...data.buckets].sort((a, b) => b[metric] - a[metric])[0];
}

function inputFor(data, question) {
  const compact = {
    theme: data.theme,
    totalCount: 3360,
    totalHouseholds: 3360,
    leaders: {
      average: leader(data, 'avg'),
      median: leader(data, 'median'),
      p95: leader(data, 'p95'),
    },
    buckets: data.buckets,
  };

  return [
    '선택된 분석 테마:',
    data.theme,
    '',
    '사용자 질문:',
    question,
    '',
    '집계 데이터(JSON):',
    JSON.stringify(compact),
    '',
    '답변 규칙:',
    '- 한국어 존댓말로 답변하세요.',
    '- 한자와 중국어 표현을 쓰지 마세요.',
    '- 단위는 kWh 또는 kWh/15분으로 쓰세요.',
    '- 질문에 대한 답변만 1~2문장으로 하세요.',
    '- 데이터에 없는 원인은 단정하지 마세요.',
  ].join('\n');
}

const rows = [];
function add(data, question, answer) {
  rows.push({
    instruction: '아래 Elasticsearch 집계 데이터를 보고 사용자 질문에 답하세요.',
    input: inputFor(data, question),
    output: answer,
  });
}

